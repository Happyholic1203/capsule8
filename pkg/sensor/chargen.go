package sensor

import (
	"fmt"

	"github.com/Happyholic1203/capsule8/pkg/expression"
)

// ChargenEventTypes defines the field types that can be used with filters on
// chargen telemetry events.
var ChargenEventTypes = expression.FieldTypeMap{
	"index":      expression.ValueTypeUnsignedInt64,
	"characters": expression.ValueTypeString,
}

// ChargenTelemetryEvent is a telemetry event generated by the chargen event
// source.
type ChargenTelemetryEvent struct {
	TelemetryEventData

	Index      uint64
	Characters string
}

// CommonTelemetryEventData returns the telemtry event data common to all
// telemetry events for a chargen telemetry event.
func (e ChargenTelemetryEvent) CommonTelemetryEventData() TelemetryEventData {
	return e.TelemetryEventData
}

func (s *Subscription) dispatchChargenEvent(
	eventid uint64,
	data expression.FieldValueMap,
) {
	var e ChargenTelemetryEvent

	e.Init(s.sensor)
	e.Index = data["index"].(uint64)
	e.Characters = data["characters"].(string)
	s.DispatchEvent(eventid, e, data)
}

func generateCharacters(start, length uint64) string {
	bytes := make([]byte, length)
	for i := uint64(0); i < length; i++ {
		bytes[i] = ' ' + byte((start+i)%95)
	}
	return string(bytes)
}

// RegisterChargenEventFilter registers a character generation event filter
// with a subscription.
func (s *Subscription) RegisterChargenEventFilter(
	length uint64,
	filter *expression.Expression,
) {
	if length == 0 || length > 1<<16 {
		s.logStatus(
			fmt.Sprintf("Chargen length out of range (%d)", length))
		return
	}

	monitor := s.sensor.Monitor()
	eventID := monitor.ReserveEventID()

	done := make(chan struct{})

	es, err := s.addEventSink(eventID, filter)
	if err != nil {
		s.logStatus(
			fmt.Sprintf("Invalid filter expression for chargen filter: %v", err))
		return
	}
	es.unregister = func(es *eventSink) {
		close(done)
	}
	es.enable = func() {
		go func() {
			index := uint64(0)
			for {
				select {
				case <-done:
					return
				default:
					characters := generateCharacters(index,
						length)
					data := expression.FieldValueMap{
						"index":      index,
						"characters": characters,
					}
					index += length
					s.dispatchChargenEvent(eventID, data)
				}
			}
		}()
	}
}
